package gr.aueb.cf.ch2;

import java.lang.*;

/**
 * Math class demo
 */
public class MathDemo {

    public static void main(String[] args) {
        System.out.println("Το απόλυτο του -5 είναι: " + Math.abs(-5));
        System.out.println("Το min του 3, 19 είναι: " + Math.min(3, 19));
        System.out.println("Το max του -5, 299 είναι: " + Math.max(-5, 299));
    }
}
